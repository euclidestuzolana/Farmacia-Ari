                        O consumo regular de vitaminas e minerais é fundamental para o bom funcionamento do organismo, pois estes nutrientes são responsáveis pela manutenção das células e dos tecidos – pele, ossos, cabelos – e pelo combate aos radicais livres.
                        <br>
                        Com propriedades antioxidantes, ainda são capazes de proteger as estruturas celulares. Com tantas funções, são substâncias necessárias para a saúde em qualquer idade e gênero.

                        <br>
                        <strong>Importância do atendimento especializado em farmáciase drogarias</strong>
                        <br><br>
                        Uma das grandes barreiras que impedem a expansão do mercado de vitaminas no Brasil é a desinformação. O brasileiro desconhece os benefícios desse segmento e não sabe distinguir, entre tantos SKUs (a sigla que representa o termo Stock Keeping Unit, em português, Unidade de Manutenção de Estoque, é definida como um identificador único de um produto e é utilizada para manutenção de estoque), quais produtos atendem a suas necessidades nutricionais.
                        Para que a equipe de atendimento esteja preparada para oferecer atendimento de qualidade em uma categoria que ainda carece de informação, o melhor caminho é procurar parceria com indústrias que ofereçam treinamento de vendas.
                        Com a capacitação correta, o atendente será capaz de entender o perfil do cliente e indicar o produto que mais se encaixa ao seu estilo de vida e objetivo.
                        Outro ponto importante é trabalhar com itens de variadas faixas de preço e, durante o atendimento, apresentar diferentes opções de desembolso dentro de uma mesma subcategoria.
                        O consumidor tem a percepção de que a categoria de vitaminas e suplementos é cara. É preciso deixar claro que existem opções acessíveis.
                        <br>
                        Os suplementos mais consumidos no País são os multivitamínicos, seguidos de ácidos graxos (especialmente o ômega 3), minerais (principalmente cálcio) e vitaminas específicas (a vitamina C é a campeã).

                        Em percentuais, 48% são vitaminas; 22%, minerais; 19% são extraídos de plantas; 17%, ácidos graxos; 16%, proteínas; 14%, aminoácidos; e 12% são de óleos, como cártamo, peixe, alho, etc.

                        Entre as razões que levam os brasileiros a comprar vitaminas e suplementos se destacam o desejo de complementar a alimentação (75%) e a busca por mais energia e aumento de massa muscular (57%).

                        Os entrevistados pela Abiad ainda afirmam que usam vitaminas e suplementos para melhorar a saúde (58%), prevenir doenças (29%), tratar alguma enfermidade (13%).
